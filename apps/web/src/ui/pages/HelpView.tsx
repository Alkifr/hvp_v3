function PermBadge(props: { perm?: string; permissions: string[] }) {
  if (!props.perm) return null;
  const has = props.permissions.includes(props.perm);
  const label = PERM_LABEL[props.perm] ?? props.perm;
  return (
    <span className={has ? "helpPerm helpPermOk" : "helpPerm helpPermLock"} title={has ? "У вас есть это право" : "Требуется право доступа"}>
      {has ? "Доступно вам" : `Нужно право: ${label}`}
    </span>
  );
}

function TopicCard(props: {
  topic: Topic;
  permissions: string[];
  open: boolean;
  onToggle: () => void;
  onNavigate: (page: HelpNavPage) => void;
  showSection?: string;
}) {
  const { topic, permissions, open, onToggle, onNavigate } = props;
  return (
    <div className={open ? "helpTopic helpTopicOpen" : "helpTopic"}>
      <button type="button" className="helpTopicHead" onClick={onToggle} aria-expanded={open}>
        <span className="helpTopicTitle">
          {topic.title}
          {props.showSection ? <span className="helpTopicCrumb">{props.showSection}</span> : null}
        </span>
        <span className="helpTopicHeadRight">
          <PermBadge perm={topic.perm} permissions={permissions} />
          <span className="helpChevron" aria-hidden="true">
            {open ? "−" : "+"}
          </span>
        </span>
      </button>
      {open ? (
        <div className="helpTopicBody">
          {topic.paragraphs?.map((p, i) => (
            <p key={i}>{p}</p>
          ))}
          {topic.bullets ? (
            <ul className="helpBullets">
              {topic.bullets.map((b, i) => (
                <li key={i}>{b}</li>
              ))}
            </ul>
          ) : null}
          {topic.steps ? (
            <ol className="helpSteps">
              {topic.steps.map((s, i) => (
                <li key={i}>
                  <span className="helpStepLabel">{s.label}</span>
                  {s.detail ? <span className="helpStepDetail">{s.detail}</span> : null}
                </li>
              ))}
            </ol>
          ) : null}
          {topic.note ? <div className="helpNote">{topic.note}</div> : null}
          {topic.go ? (
            <button type="button" className="btn btnPrimary helpGoBtn" onClick={() => onNavigate(topic.go!.page)}>
              {topic.go.label} →
            </button>
          ) : null}
        </div>
      ) : null}
    </div>
  );
}

function ScenarioPlayer(props: { scenario: Scenario; onNavigate: (page: HelpNavPage) => void }) {
  const { scenario, onNavigate } = props;
  const [step, setStep] = useState(0);
  const total = scenario.steps.length;
  const current = scenario.steps[step]!;
  const progress = Math.round(((step + 1) / total) * 100);

  return (
    <div className="helpScenario card">
      <div className="helpScenarioHead">
        <div>
          <div className="helpScenarioAudience">{scenario.audience}</div>
          <h3>{scenario.title}</h3>
          <p className="muted">{scenario.goal}</p>
        </div>
        <span className="helpScenarioCounter">
          Шаг {step + 1} / {total}
        </span>
      </div>

      <div className="helpProgress" aria-hidden="true">
        <div className="helpProgressBar" style={{ width: `${progress}%` }} />
      </div>

      <div className="helpScenarioStep">
        <span className="helpScenarioStepNo">{step + 1}</span>
        <div className="helpScenarioStepText">
          <p>{current.text}</p>
          {current.go ? (
            <button type="button" className="btn btnGhost" onClick={() => onNavigate(current.go!.page)}>
              {current.go.label} →
            </button>
          ) : null}
        </div>
      </div>

      <div className="helpScenarioDots">
        {scenario.steps.map((_, i) => (
          <button
            key={i}
            type="button"
            className={i === step ? "helpDot helpDotActive" : i < step ? "helpDot helpDotDone" : "helpDot"}
            onClick={() => setStep(i)}
            aria-label={`Шаг ${i + 1}`}
          />
        ))}
      </div>

      <div className="helpScenarioNav">
        <button type="button" className="btn" disabled={step === 0} onClick={() => setStep((s) => Math.max(0, s - 1))}>
          ← Назад
        </button>
        {step < total - 1 ? (
          <button type="button" className="btn btnPrimary" onClick={() => setStep((s) => Math.min(total - 1, s + 1))}>
            Далее →
          </button>
        ) : (
          <button type="button" className="btn btnPrimary" onClick={() => setStep(0)}>
            Начать заново
          </button>
        )}
      </div>
    </div>
  );
}

export function HelpView(props: { permissions: string[]; onNavigate: (page: HelpNavPage) => void }) {
  const { permissions, onNavigate } = props;
  const { active: activeSandbox } = useActiveSandbox();

  const NAV: { id: string; title: string; icon: ReactNode }[] = useMemo(
    () => [
      ...SECTIONS.map((s) => ({ id: s.id, title: s.title, icon: s.icon })),
      { id: "scenarios", title: "Сценарии", icon: ICONS.scenarios },
      { id: "faq", title: "Частые вопросы", icon: ICONS.faq }
    ],
    []
  );

  const [activeId, setActiveId] = useState<string>("start");
  const [query, setQuery] = useState("");
  const [openTopics, setOpenTopics] = useState<Record<string, boolean>>({});

  const toggleTopic = (id: string) =>
    setOpenTopics((prev) => ({ ...prev, [id]: !prev[id] }));

  const normalizedQuery = query.trim().toLowerCase();

  const searchResults = useMemo(() => {
    if (!normalizedQuery) return null;
    const out: { topic: Topic; sectionTitle: string }[] = [];
    for (const section of SECTIONS) {
      for (const topic of section.topics) {
        const haystack = [
          topic.title,
          ...(topic.paragraphs ?? []),
          ...(topic.bullets ?? []),
          ...(topic.steps?.map((s) => `${s.label} ${s.detail ?? ""}`) ?? []),
          topic.note ?? "",
          ...(topic.keywords ?? [])
        ]
          .join(" ")
          .toLowerCase();
        if (haystack.includes(normalizedQuery)) {
          out.push({ topic, sectionTitle: section.title });
        }
      }
    }
    return out;
  }, [normalizedQuery]);

  const activeSection = SECTIONS.find((s) => s.id === activeId) ?? null;

  return (
    <div className="helpPage">
      <section className="massHero helpHero">
        <div className="massHeroText">
          <div className="massEyebrow">Инструкция</div>
          <h1>Как работать с приложением</h1>
          <p>
            Интерактивное руководство по Hangar Planning: возможности разделов, пошаговые сценарии и ответы на частые
            вопросы. Используйте поиск или перейдите к нужному разделу.
          </p>
        </div>
        <div className="massHeroStats" aria-label="Контекст">
          <span>
            <b>{activeSandbox ? "Песочница" : "Рабочий контур"}</b>
          </span>
          <span>
            <b>{permissions.length}</b> прав доступа
          </span>
        </div>
      </section>

      <div className="helpSearchRow card">
        <span className="helpSearchIcon" aria-hidden="true">
          <svg viewBox="0 0 24 24" width="18" height="18" fill="none" stroke="currentColor" strokeWidth="1.8" strokeLinecap="round" strokeLinejoin="round">
            <circle cx="11" cy="11" r="7" />
            <path d="m21 21-4.3-4.3" />
          </svg>
        </span>
        <input
          value={query}
          onChange={(e) => setQuery(e.target.value)}
          placeholder="Поиск по инструкции: например, «резерв», «импорт», «пароль»…"
          aria-label="Поиск по инструкции"
        />
        {query ? (
          <button type="button" className="btn btnGhost" onClick={() => setQuery("")}>
            Очистить
          </button>
        ) : null}
      </div>

      {searchResults ? (
        <div className="helpSearchResults">
          <div className="helpSectionIntro">
            Найдено тем: <b>{searchResults.length}</b> по запросу «{query}».
          </div>
          {searchResults.length === 0 ? (
            <div className="card muted">Ничего не найдено. Попробуйте другой запрос.</div>
          ) : (
            <div className="helpTopics">
              {searchResults.map(({ topic, sectionTitle }) => (
                <TopicCard
                  key={topic.id}
                  topic={topic}
                  permissions={permissions}
                  open={openTopics[topic.id] ?? true}
                  onToggle={() => toggleTopic(topic.id)}
                  onNavigate={onNavigate}
                  showSection={sectionTitle}
                />
              ))}
            </div>
          )}
        </div>
      ) : (
        <div className="helpLayout">
          <aside className="helpNav">
            {NAV.map((n) => (
              <button
                key={n.id}
                type="button"
                className={activeId === n.id ? "helpNavItem helpNavItemActive" : "helpNavItem"}
                onClick={() => setActiveId(n.id)}
              >
                <span className="helpNavIcon" aria-hidden="true">
                  {n.icon}
                </span>
                <span>{n.title}</span>
              </button>
            ))}
          </aside>

          <div className="helpContent">
            {activeSection ? (
              <>
                <div className="helpSectionHead">
                  <h2>{activeSection.title}</h2>
                  <p className="muted">{activeSection.intro}</p>
                </div>
                <div className="helpTopics">
                  {activeSection.topics.map((topic) => (
                    <TopicCard
                      key={topic.id}
                      topic={topic}
                      permissions={permissions}
                      open={openTopics[topic.id] ?? false}
                      onToggle={() => toggleTopic(topic.id)}
                      onNavigate={onNavigate}
                    />
                  ))}
                </div>
              </>
            ) : null}

            {activeId === "scenarios" ? (
              <>
                <div className="helpSectionHead">
                  <h2>Пошаговые сценарии</h2>
                  <p className="muted">
                    Выберите задачу — руководство проведёт по шагам. Кнопки переходят в нужный раздел приложения.
                  </p>
                </div>
                <div className="helpScenarioGrid">
                  {SCENARIOS.map((sc) => (
                    <ScenarioPlayer key={sc.id} scenario={sc} onNavigate={onNavigate} />
                  ))}
                </div>
              </>
            ) : null}

            {activeId === "faq" ? (
              <>
                <div className="helpSectionHead">
                  <h2>Частые вопросы</h2>
                  <p className="muted">Короткие ответы на типичные ситуации.</p>
                </div>
                <div className="helpTopics">
                  {FAQ.map((item, i) => (
                    <div className={openTopics[`faq-${i}`] ? "helpTopic helpTopicOpen" : "helpTopic"} key={i}>
                      <button
                        type="button"
                        className="helpTopicHead"
                        onClick={() => toggleTopic(`faq-${i}`)}
                        aria-expanded={openTopics[`faq-${i}`] ?? false}
                      >
                        <span className="helpTopicTitle">{item.q}</span>
                        <span className="helpChevron" aria-hidden="true">
                          {openTopics[`faq-${i}`] ? "−" : "+"}
                        </span>
                      </button>
                      {openTopics[`faq-${i}`] ? (
                        <div className="helpTopicBody">
                          <p>{item.a}</p>
                        </div>
                      ) : null}
                    </div>
                  ))}
                </div>
              </>
            ) : null}
          </div>
        </div>
      )}
    </div>
  );
}
